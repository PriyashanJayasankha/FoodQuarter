    private int checkEmail(String x) throws SQLException{
        String q1 = "SELECT email FROM customers WHERE email = ?";
        String q2 = "SELECT email FROM sellers WHERE email = ?";
        
        pst4 = con.prepareStatement(q1);
        pst4.setString(1, x);
        rs1 = pst4.executeQuery();
        
        pst5 = con.prepareStatement(q2);
        pst5.setString(1, x);
        rs2 = pst5.executeQuery();
        
        Boolean cus , sel;
        
        cus = rs1.next();
        sel = rs2.next();
        
        if(cus&&sel){
            //registerd as cus & sel
            return 3;
        }
        else if(cus){
            //registerd as cus
            return 1;
        }
        else if(sel){
            //registerd as sel
            return 2;
        }
        //not registerd
        return 0;
    }
